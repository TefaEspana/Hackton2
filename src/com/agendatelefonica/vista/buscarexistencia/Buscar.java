    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(nombre.getText().isEmpty() || apellido.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "❌ Complete los campos de nombre y apellido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Contactos contacto = new Contactos(nombre.getText(),apellido.getText());
        boolean dato = datos.getAgenda().existeContacto(contacto);
        if(dato){
            JOptionPane.showMessageDialog(this, "El contacto se encuentra en los contactos", "Información", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "❌ Contacto no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

